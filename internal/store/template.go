// fill substitutes «...» placeholders in s. Guillemets never occur in the
// Chinese markdown content, so this is safe next to mermaid braces.
func fill(s string, subs map[string]string) string {
	for k, v := range subs {
		s = strings.ReplaceAll(s, k, v)
	}
	return s
}

